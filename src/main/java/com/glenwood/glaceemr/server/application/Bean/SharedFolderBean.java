package com.glenwood.glaceemr.server.application.Bean;

public class SharedFolderBean {

	String gwtLanURL;
	String accountId;
	String springURL;
	String tomCatURL;
	String springPublicURL;
	String sharedFolderPath;
	String apacheURL;
	
	public String getGwtLanURL() {
		return gwtLanURL;
	}
	public void setGwtLanURL(String gwtLanURL) {
		this.gwtLanURL = gwtLanURL;
	}
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	public String getSpringURL() {
		return springURL;
	}
	public void setSpringURL(String springURL) {
		this.springURL = springURL;
	}
	public String getTomCatURL() {
		return tomCatURL;
	}
	public void setTomCatURL(String tomCatURL) {
		this.tomCatURL = tomCatURL;
	}
	public String getSpringPublicURL() {
		return springPublicURL;
	}
	public void setSpringPublicURL(String springPublicURL) {
		this.springPublicURL = springPublicURL;
	}
	public String getSharedFolderPath() {
		return sharedFolderPath;
	}
	public void setSharedFolderPath(String sharedFolderPath) {
		this.sharedFolderPath = sharedFolderPath;
	}
	public String getApacheURL() {
		return apacheURL;
	}
	public void setApacheURL(String apacheURL) {
		this.apacheURL = apacheURL;
	}
	
	
}
